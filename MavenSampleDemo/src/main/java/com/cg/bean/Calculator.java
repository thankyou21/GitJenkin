package com.cg.bean;

public class Calculator {
	
	public int add(int num1, int num2)
	{
		return num1+num2;
	}
	
	
	public int sub(int num1, int num2)
	{
		return num1-num2;
	}
	
	public int mul(int num1, int num2)
	{
		return num1*num2;
	}
	
	public int square(int num1)
	{
		return num1*num1;
	}
}
